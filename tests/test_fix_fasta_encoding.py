"""Tests for scripts/fix_fasta_encoding.py"""

import os
import unittest
import importlib.util

SCRIPTS_DIR = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), 'scripts')

def _load_module(script_name: str):
    spec = importlib.util.spec_from_file_location(
        script_name.replace('.py', ''),
        os.path.join(SCRIPTS_DIR, script_name),
    )
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod

class TestFixFastaEncoding(unittest.TestCase):
    @classmethod
    def setUpClass(cls):
        cls.mod = _load_module("fix_fasta_encoding.py")

    def test_strip_c0_controls_tab_conversion(self):
        """Literal TAB characters (\\t) should be converted to standard spaces."""
        s = "Spike|hCoV-19/Australia/2024\t|Original"
        # Since _decode_line calls _strip_c0_controls:
        fixed = self.mod._strip_c0_controls(s)
        self.assertEqual(fixed, "Spike|hCoV-19/Australia/2024 |Original")

    def test_strip_c0_controls_etx_removal(self):
        """ASCII control characters like ETX (\\x03) should be stripped entirely."""
        s = "Spike\x03_Variant"
        fixed = self.mod._strip_c0_controls(s)
        self.assertEqual(fixed, "Spike_Variant")

        # Test a combination
        s_combo = "Header\tWith\x01Control\tTokens"
        fixed_combo = self.mod._strip_c0_controls(s_combo)
        self.assertEqual(fixed_combo, "Header WithControl Tokens")

    def test_decode_line_escapes_and_controls(self):
        """Full _decode_line function tests including \\u escapes + tabs + limits."""
        # \u0003 is exactly what is generated by Java tools for ETX
        raw = b">Some\\u0003Header\tName"
        decoded = self.mod._decode_line(raw)
        self.assertEqual(decoded, ">SomeHeader Name")

if __name__ == '__main__':
    unittest.main()
